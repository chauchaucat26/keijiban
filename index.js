async function resolvePartials(reaction, user) {
  try {
    if (reaction.partial) await reaction.fetch();
  } catch (e) {
    await sendErrorLog('リアクションのfetch失敗', e);
    return false;
  }
  try {
    if (user.partial) await user.fetch();
  } catch (e) {
    await sendErrorLog('ユーザーのfetch失敗', e);
    return false;
  }
  return true;
}

client.on('messageReactionAdd', async (reaction, user) => {
  if (user.bot) return;
  const ok = await resolvePartials(reaction, user);
  if (!ok) return;

  const message = reaction.message;
  if (!message.guild) return;

  const emojiKey = emojiKeyFromReactionEmoji(reaction.emoji);
  const roleId = await getReactionRoleMapping(message.id, emojiKey);
  if (!roleId) return; // 登録されていないリアクションは無視

  const member = await message.guild.members.fetch(user.id).catch(() => null);
  const role = member ? await message.guild.roles.fetch(roleId).catch(() => null) : null;
  if (!member || !role) return;

  try {
    await member.roles.add(role);
    console.log(`✅ ${user.tag} に ${role.name} を付与`);
  } catch (e) {
    await sendErrorLog('リアクションロール付与失敗', e, {
      user: `${user.tag} (${user.id})`,
      role: role.id,
      messageId: message.id,
    });
  }
});

client.on('messageReactionRemove', async (reaction, user) => {
  if (user.bot) return;
  const ok = await resolvePartials(reaction, user);
  if (!ok) return;

  const message = reaction.message;
  if (!message.guild) return;

  const emojiKey = emojiKeyFromReactionEmoji(reaction.emoji);
  const roleId = await getReactionRoleMapping(message.id, emojiKey);
  if (!roleId) return;

  const member = await message.guild.members.fetch(user.id).catch(() => null);
  const role = member ? await message.guild.roles.fetch(roleId).catch(() => null) : null;
  if (!member || !role) return;

  try {
    await member.roles.remove(role);
    console.log(`🔻 ${user.tag} から ${role.name} を剥奪`);
  } catch (e) {
    await sendErrorLog('リアクションロール剥奪失敗', e, {
      user: `${user.tag} (${user.id})`,
      role: role.id,
      messageId: message.id,
    });
  }
});

