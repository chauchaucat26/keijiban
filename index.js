client.on('messageCreate', async (message) => {
  if (message.author.bot) return;
  if (!message.guild) return; // コマンド類はサーバー内のみ対象

  const content = message.content.trim();

  /* ---- 管理者向けコマンド ---- */

  // !setmentionrole @ロール または ロールID
  if (content.startsWith('!setmentionrole')) {
    if (!isAdmin(message.member)) {
      return message.reply('この操作には管理者権限が必要だよ。');
    }
    const roleId = extractRoleId(content.replace('!setmentionrole', '').trim());
    if (!roleId) {
      return message.reply('使い方: `!setmentionrole @ロール名`');
    }
    const role = await message.guild.roles.fetch(roleId).catch(() => null);
    if (!role) return message.reply('そのロールが見つからなかったよ。');

    const { ok, error } = await setMentionRole(message.guild.id, roleId);
    if (!ok) {
      await sendErrorLog('メンションロール設定の保存失敗', error, {
        guild: message.guild.name,
        roleId,
      });
    }
    return message.reply(
      ok
        ? `BOTがメンションされたら「${role.name}」を自動付与するように設定したよ！`
        : '設定の保存に失敗しちゃった…もう一回試してみて。'
    );
  }

  // !addreactrole <messageId> <絵文字> @ロール
  if (content.startsWith('!addreactrole')) {
    if (!isAdmin(message.member)) {
      return message.reply('この操作には管理者権限が必要だよ。');
    }
    const parts = content.split(/\s+/);
    if (parts.length < 4) {
      return message.reply('使い方: `!addreactrole <メッセージID> <絵文字> @ロール`');
    }
    const targetMessageId = parts[1];
    const emojiKey = extractEmojiKey(parts[2]);
    const roleId = extractRoleId(parts.slice(3).join(' '));

    if (!emojiKey) return message.reply('絵文字が正しく認識できなかったよ。');
    if (!roleId) return message.reply('ロールが正しく指定されていないよ。');

    const targetMessage = await message.channel.messages.fetch(targetMessageId).catch(() => null);
    if (!targetMessage) {
      return message.reply('指定されたメッセージIDがこのチャンネル内で見つからなかったよ。');
    }

    // BOT自身が先にリアクションしておく（ユーザーが押しやすいように）
    try {
      const customEmojiMatch = parts[2].match(/<a?:(\w+):(\d+)>/);
      if (customEmojiMatch) {
        await targetMessage.react(customEmojiMatch[2]);
      } else {
        await targetMessage.react(parts[2]);
      }
    } catch (e) {
      await sendErrorLog('初期リアクション付与に失敗', e, {
        messageId: targetMessageId,
        emoji: parts[2],
      });
    }

    const { ok, error } = await addReactionRoleMapping(message.guild.id, targetMessageId, emojiKey, roleId);
    if (!ok) {
      await sendErrorLog('リアクションロール登録失敗', error, {
        guild: message.guild.name,
        messageId: targetMessageId,
        emojiKey,
        roleId,
      });
    }
    return message.reply(
      ok
        ? 'リアクションロールを登録したよ！このリアクションを付けると対象ロールが付与されるよ。'
        : '登録に失敗しちゃった…もう一回試してみて。'
    );
  }

  // !removereactrole <messageId> <絵文字>
  if (content.startsWith('!removereactrole')) {
    if (!isAdmin(message.member)) {
      return message.reply('この操作には管理者権限が必要だよ。');
    }
    const parts = content.split(/\s+/);
    if (parts.length < 3) {
      return message.reply('使い方: `!removereactrole <メッセージID> <絵文字>`');
    }
    const targetMessageId = parts[1];
    const emojiKey = extractEmojiKey(parts[2]);
    if (!emojiKey) return message.reply('絵文字が正しく認識できなかったよ。');

    const { ok, error } = await removeReactionRoleMapping(targetMessageId, emojiKey);
    if (!ok) {
      await sendErrorLog('リアクションロール解除失敗', error, {
        guild: message.guild.name,
        messageId: targetMessageId,
        emojiKey,
      });
    }
    return message.reply(ok ? 'リアクションロールの登録を解除したよ。' : '解除に失敗しちゃった。');
  }

  // !listreactroles
  if (content.startsWith('!listreactroles')) {
    if (!isAdmin(message.member)) {
      return message.reply('この操作には管理者権限が必要だよ。');
    }
    const list = await listReactionRoleMappings(message.guild.id);
    if (list.length === 0) return message.reply('登録されているリアクションロールはまだないよ。');
    const lines = list.map(
      (r) => `メッセージID: ${r.message_id} / 絵文字キー: ${r.emoji_key} / ロール: <@&${r.role_id}>`
    );
    return message.reply(lines.join('\n'));
  }

  /* ---- メンションによる自動ロール付与 ----
   * BOTがメンションされたら、発言者に設定済みロールを自動付与します。 */
  if (message.mentions.has(client.user)) {
    const roleId = await getMentionRole(message.guild.id);
    if (roleId) {
      const role = await message.guild.roles.fetch(roleId).catch(() => null);
      if (role) {
        try {
          await message.member.roles.add(role);
          await message.reply(`「${role.name}」ロールを付与したよ！`);
        } catch (e) {
          await sendErrorLog('メンションロール付与失敗', e, {
            user: `${message.author.tag} (${message.author.id})`,
            role: role.id,
            guild: message.guild.name,
          });
          await message.reply('ロールの付与に失敗しちゃった…BOTの権限を確認してね。');
        }
      }
    }
  }

  /* ---- 既存の動画掲載許可フロー ---- */
  if (message.channel.id !== '1487031369430204417') return;

  const row = new ActionRowBuilder().addComponents(
    new ButtonBuilder().setCustomId('approve_post').setLabel('掲載を許可する').setStyle(ButtonStyle.Success),
    new ButtonBuilder().setCustomId('deny_post').setLabel('許可しない').setStyle(ButtonStyle.Danger)
  );

  const matchYT = message.content.match(ytRegex);
  if (matchYT) {
    const videoId = matchYT[1];
    await message.reply({
      content:
        `この作品をサーバー公式サイトに掲載しちゃう？${videoId} \n` +
        `* ※許可を頂いた場合、当サイトの[利用規約・免責事項](https://blog2.chauchaucat.f5.si/)に同意したものとみなします。\n` +
        `* ※掲載後、削除を希望される場合は @ChauChauCat へ別途お問い合わせください。`,
      components: [row],
    });
  }

  const matchNC = message.content.match(nicoRegex);
  if (matchNC) {
    const videoId = matchNC[1];
    await message.reply({
      content:
        `この作品をサーバー公式サイトに掲載しちゃう？${videoId} \n` +
        `* ※許可を頂いた場合、当サイトの[利用規約・免責事項](https://blog2.chauchaucat.f5.si/)に同意したものとみなします。\n` +
        `* ※掲載後、削除を希望される場合は @ChauChauCat へ別途お問い合わせください。`,
      components: [row],
    });
  }
});

