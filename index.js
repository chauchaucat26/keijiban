function isAdmin(member) {
  if (!member) return false;
  return member.permissions.has(PermissionsBitField.Flags.Administrator);
}

function extractRoleId(text) {
  const mentionMatch = text.match(/<@&(\d+)>/);
  if (mentionMatch) return mentionMatch[1];
  const idMatch = text.match(/^\d{15,20}$/);
  if (idMatch) return idMatch[0];
  return null;
}

// カスタム絵文字 <:name:id> / <a:name:id> / Unicode絵文字 から
// DB保存用のキーを作る。カスタム絵文字はID、それ以外はそのままの文字を使う。
function extractEmojiKey(text) {
  const customMatch = text.match(/<a?:\w+:(\d+)>/);
  if (customMatch) return customMatch[1];
  const trimmed = text.trim();
  return trimmed.length > 0 ? trimmed : null;
}

// discord.js の ReactionEmoji / GuildEmoji オブジェクトからキーを作る（イベント側で使用）
function emojiKeyFromReactionEmoji(emoji) {
  return emoji.id ? emoji.id : emoji.name;
}

/* ============================================================
 * Supabase: reaction_roles / mention_roles テーブルを利用
 *
 * 事前に以下のテーブルをSupabase上に作成してください:
 *
 * create table reaction_roles (
 *   id bigint generated by default as identity primary key,
 *   guild_id text not null,
 *   message_id text not null,
 *   emoji_key text not null,
 *   role_id text not null,
 *   created_at timestamptz default now(),
 *   unique (message_id, emoji_key)
 * );
 *
 * create table mention_roles (
 *   guild_id text primary key,
 *   role_id text not null
 * );
 * ============================================================ */

async function addReactionRoleMapping(guildId, messageId, emojiKey, roleId) {
  const { error } = await supabase
    .from('reaction_roles')
    .upsert(
      [{ guild_id: guildId, message_id: messageId, emoji_key: emojiKey, role_id: roleId }],
      { onConflict: 'message_id,emoji_key' }
    );
  return { ok: !error, error };
}

async function removeReactionRoleMapping(messageId, emojiKey) {
  const { error } = await supabase
    .from('reaction_roles')
    .delete()
    .eq('message_id', messageId)
    .eq('emoji_key', emojiKey);
  return { ok: !error, error };
}

async function getReactionRoleMapping(messageId, emojiKey) {
  const { data, error } = await supabase
    .from('reaction_roles')
    .select('role_id')
    .eq('message_id', messageId)
    .eq('emoji_key', emojiKey)
    .maybeSingle();
  if (error || !data) return null;
  return data.role_id;
}

async function listReactionRoleMappings(guildId) {
  const { data, error } = await supabase
    .from('reaction_roles')
    .select('message_id, emoji_key, role_id')
    .eq('guild_id', guildId);
  if (error) return [];
  return data;
}

async function setMentionRole(guildId, roleId) {
  const { error } = await supabase
    .from('mention_roles')
    .upsert([{ guild_id: guildId, role_id: roleId }], { onConflict: 'guild_id' });
  return { ok: !error, error };
}

async function getMentionRole(guildId) {
  const { data, error } = await supabase
    .from('mention_roles')
    .select('role_id')
    .eq('guild_id', guildId)
    .maybeSingle();
  if (error || !data) return null;
  return data.role_id;
}

